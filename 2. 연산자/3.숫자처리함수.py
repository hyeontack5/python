# 숫자처리함수

# absolute value : 절대값
print(abs(-5)) # 5
print(pow(4, 2)) # 4^2 = 4*4 = 16
print(max(5, 12)) # 12
print(min(5, 12)) # 5
print(round(3.14)) # 3
print(round(4.99)) # 5

# math 라이브러리에 있는 내용을 모두 사용하겠다는 의미이다.
from math import *
print(floor(4.99)) # 내림. 4
print(ceil(3.14)) # 올림. 4
# square root : 제곱근
print(sqrt(16)) # 제곱근. 4